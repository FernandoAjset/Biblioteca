    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed
        try{
            int ccliente=Integer.parseInt(incliente.getText());
            String nombre=mcliente.getText();
            String direccion=idir.getText();
            String nit=init.getText();
            int cproducto=Integer.parseInt(inproducto.getText());
            String nproducto=mproducto.getText();
            double pproducto=Double.parseDouble(mprecio.getText());
            int cuotas=ncuotas;
            double mcuotas=Double.parseDouble(mcuota.getText()); 
            
            ResultSet rs;
            Statement existetabla = Conexiones.Conexion.getConexion().createStatement();
            rs = existetabla.executeQuery("select cfactura from factura");
            rs = existetabla.executeQuery("select ccontrato from contrato");
            
            proyectopagos.MetodosDocumentos.GrabarNuevaFactura(ccliente, nombre, direccion, nit, cproducto,
            nproducto, pproducto, cuotas, mcuotas);
            cfactura=MetodosDocumentos.CodigoUltimaFactura();
            
            proyectopagos.MetodosDocumentos.GrabarContrato(ccliente,cfactura,cuotas,mcuotas,pproducto);
            ccontrato=MetodosDocumentos.CodigoUltimoContrato();
            
            proyectopagos.MetodosDocumentos.GrabarFechas(ccontrato,cuotas);
            ingreso();
        }
        catch(NumberFormatException ex)
        {
                JOptionPane.showMessageDialog(null, "ERROR AL INGRESAR DATOS, VERIFIQUE");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "La BD tiene problemas" + e.getMessage(),
            "Error de Base de Datos",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ingresarActionPerformed

