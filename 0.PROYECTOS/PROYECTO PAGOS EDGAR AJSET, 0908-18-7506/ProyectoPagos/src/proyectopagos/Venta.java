    private void inclienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inclienteKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
            try
            {
                int id=Integer.parseInt(incliente.getText());
                String nombre = new String();
                String apellido = new String();
                String direccion = new String();
                String nit = new String();

                boolean r;
                r=MetodosCliente.ExisteCodigoCliente(id);
                if(r==true)
                {
                    Statement ps=null;
                    ResultSet rs;

                    ps = Conexiones.Conexion.getConexion().createStatement();
                    rs = ps.executeQuery("select ccliente,ncliente,acliente,dcliente,nitcliente from clientes");
                    while (rs.next())
                    {
                        String ccliente = rs.getString("CCLIENTE");
                        int auxcodigo=Integer.parseInt(ccliente);
                        String n = rs.getString("NCLIENTE");
                        String a = rs.getString("ACLIENTE");
                        String d = rs.getString("DCLIENTE");
                        String ni = rs.getString("NITCLIENTE");
                        if(id==auxcodigo)
                        {
                            nombre=n;
                            apellido=a;
                            direccion=d;
                            nit=ni;
                        }
                    }
                    mcliente.setText(nombre+" "+apellido);
                    idir.setText(direccion);
                    init.setText(nit);
                    inproducto.requestFocus();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "CODIGO NO ENCONTRADO, VERIFIQUE");
                    incliente.requestFocus();
                    mcliente.setText("");
                    idir.setText("");
                    init.setText("");
                }
            }
            catch(NumberFormatException ex)
            {
                JOptionPane.showMessageDialog(null, "ERROR AL INGRESAR DATOS, VERIFIQUE");
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null, "La BD tiene problemas" + e.getMessage(),
                    "Error de Base de Datos",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_inclienteKeyPressed

