    private void grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grabarActionPerformed
        // TODO add your handling code here:
        
        try{
        String nombre=inombre.getText();
        String apellido=iapellido.getText();
        String direccion=idireccion.getText();
        String nit=init.getText();
        int cel=Integer.parseInt(icelular.getText());

        ResultSet rs;
        Statement existetabla = Conexiones.Conexion.getConexion().createStatement();
        rs = existetabla.executeQuery("select ccliente from clientes");
        MetodosCliente.GrabarNuevoCliente(nombre,apellido,direccion,nit,cel);
        ingreso();
        }
        catch(NumberFormatException ex)
        {
                JOptionPane.showMessageDialog(null, "ERROR AL INGRESAR DATOS, VERIFIQUE");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "La BD tiene problemas" + e.getMessage(),
            "Error de Base de Datos",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_grabarActionPerformed


